public class C {
    public static void main(String[] args) {
        int x, y;
        x = 10;
        y = 20;

        if(x < y) System.out.println("Значение x меньше y.");
        x *= 2;
        if(x == y) System.out.println("Теперь значение x равно y.");
        x *= 2;
        if(x > y) System.out.println("Теперь значение x больше y.");

    }
}
